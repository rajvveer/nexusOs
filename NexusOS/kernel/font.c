/* --------------------------------------------------------------------------
 * font_get_glyph: Return pointer to glyph bitmap for active font
 * -------------------------------------------------------------------------- */
const uint8_t* font_get_glyph(uint8_t c) {
    if (active_font_size == FONT_SIZE_8x8)
        return font8x8_data[c];
    return font_data[c];
}

/* --------------------------------------------------------------------------
 * Font size selection
 * -------------------------------------------------------------------------- */
void font_set_size(FontSize size) { active_font_size = size; }
FontSize font_get_size(void) { return active_font_size; }
int font_get_active_width(void) { return FONT_WIDTH; }
int font_get_active_height(void) {
    return (active_font_size == FONT_SIZE_8x8) ? FONT8_HEIGHT : FONT_HEIGHT;
}

/* --------------------------------------------------------------------------
 * font_draw_char: Render a single character at pixel position
 * -------------------------------------------------------------------------- */
void font_draw_char(int x, int y, uint8_t c, uint32_t fg, uint32_t bg) {
    if (!fb_is_vesa()) return;
    uint32_t fb_w = fb_get_width();
    uint32_t fb_h = fb_get_height();
    int h = font_get_active_height();
    if (x < 0 || x + FONT_WIDTH > (int)fb_w || y < 0 || y + h > (int)fb_h) return;

    uint32_t* backbuf = fb_get_backbuffer();
    if (!backbuf) return;

    const uint8_t* glyph = font_get_glyph(c);

    for (int row = 0; row < h; row++) {
        uint8_t bits = glyph[row];
        uint32_t* dst = &backbuf[(y + row) * fb_w + x];
        dst[0] = (bits & 0x80) ? fg : bg;
        dst[1] = (bits & 0x40) ? fg : bg;
        dst[2] = (bits & 0x20) ? fg : bg;
        dst[3] = (bits & 0x10) ? fg : bg;
        dst[4] = (bits & 0x08) ? fg : bg;
        dst[5] = (bits & 0x04) ? fg : bg;
        dst[6] = (bits & 0x02) ? fg : bg;
        dst[7] = (bits & 0x01) ? fg : bg;
    }
}

/* --------------------------------------------------------------------------
 * font_draw_char_transparent: Render character, skip bg pixels
 * -------------------------------------------------------------------------- */
void font_draw_char_transparent(int x, int y, uint8_t c, uint32_t fg) {
    if (!fb_is_vesa()) return;
    uint32_t fb_w = fb_get_width();
    uint32_t fb_h = fb_get_height();
    int h = font_get_active_height();
    if (x < 0 || x + FONT_WIDTH > (int)fb_w || y < 0 || y + h > (int)fb_h) return;

    uint32_t* backbuf = fb_get_backbuffer();
    if (!backbuf) return;

    const uint8_t* glyph = font_get_glyph(c);

    for (int row = 0; row < h; row++) {
        uint8_t bits = glyph[row];
        uint32_t* dst = &backbuf[(y + row) * fb_w + x];
        if (bits & 0x80) dst[0] = fg;
        if (bits & 0x40) dst[1] = fg;
        if (bits & 0x20) dst[2] = fg;
        if (bits & 0x10) dst[3] = fg;
        if (bits & 0x08) dst[4] = fg;
        if (bits & 0x04) dst[5] = fg;
        if (bits & 0x02) dst[6] = fg;
        if (bits & 0x01) dst[7] = fg;
    }
}

/* --------------------------------------------------------------------------
 * font_draw_string: Render a null-terminated string at pixel position
 * -------------------------------------------------------------------------- */
void font_draw_string(int x, int y, const char* str, uint32_t fg, uint32_t bg) {
    int px = x;
    while (*str) {
        font_draw_char(px, y, (uint8_t)*str, fg, bg);
        px += FONT_WIDTH;
        str++;
    }
}

/* ==========================================================================
 * Phase 15: Scaled Rendering
 * ========================================================================== */

void font_draw_char_scaled(int x, int y, uint8_t c, uint32_t fg, uint32_t bg, int scale) {
    if (!fb_is_vesa() || scale < 1 || scale > FONT_MAX_SCALE) return;
    if (scale == 1) { font_draw_char(x, y, c, fg, bg); return; }

    uint32_t fb_w = fb_get_width();
    uint32_t fb_h = fb_get_height();
    int h = font_get_active_height();
    int sw = FONT_WIDTH * scale;
    int sh = h * scale;
    if (x < 0 || x + sw > (int)fb_w || y < 0 || y + sh > (int)fb_h) return;

    uint32_t* backbuf = fb_get_backbuffer();
    if (!backbuf) return;

    const uint8_t* glyph = font_get_glyph(c);

    for (int row = 0; row < h; row++) {
        uint8_t bits = glyph[row];
        for (int sy = 0; sy < scale; sy++) {
            uint32_t* dst = &backbuf[(y + row * scale + sy) * fb_w + x];
            for (int col = 0; col < 8; col++) {
                uint32_t color = (bits & (0x80 >> col)) ? fg : bg;
                for (int sx = 0; sx < scale; sx++)
                    dst[col * scale + sx] = color;
            }
        }
    }
}

void font_draw_char_scaled_transparent(int x, int y, uint8_t c, uint32_t fg, int scale) {
    if (!fb_is_vesa() || scale < 1 || scale > FONT_MAX_SCALE) return;
    if (scale == 1) { font_draw_char_transparent(x, y, c, fg); return; }

    uint32_t fb_w = fb_get_width();
    uint32_t fb_h = fb_get_height();
    int h = font_get_active_height();
    int sw = FONT_WIDTH * scale;
    int sh = h * scale;
    if (x < 0 || x + sw > (int)fb_w || y < 0 || y + sh > (int)fb_h) return;

    uint32_t* backbuf = fb_get_backbuffer();
    if (!backbuf) return;

    const uint8_t* glyph = font_get_glyph(c);

    for (int row = 0; row < h; row++) {
        uint8_t bits = glyph[row];
        for (int sy = 0; sy < scale; sy++) {
            uint32_t* dst = &backbuf[(y + row * scale + sy) * fb_w + x];
            for (int col = 0; col < 8; col++) {
                if (bits & (0x80 >> col)) {
                    for (int sx = 0; sx < scale; sx++)
                        dst[col * scale + sx] = fg;
                }
            }
        }
    }
}

void font_draw_string_scaled(int x, int y, const char* str, uint32_t fg, uint32_t bg, int scale) {
    if (scale < 1) scale = 1;
    int px = x;
    while (*str) {
        font_draw_char_scaled(px, y, (uint8_t)*str, fg, bg, scale);
        px += FONT_WIDTH * scale;
        str++;
    }
}

/* ==========================================================================
 * Phase 15: Anti-Aliased Rendering (2x2 supersampling)
 * ========================================================================== */

/* Blend helper: mix fg and bg by coverage (0=bg, 1..4=partial..full fg) */
static inline uint32_t blend_colors(uint32_t fg, uint32_t bg, int coverage) {
    if (coverage == 0) return bg;
    if (coverage == 4) return fg;
    /* Integer blending: coverage/4 ratio */
    uint32_t fr = (fg >> 16) & 0xFF, fg_g = (fg >> 8) & 0xFF, fb = fg & 0xFF;
    uint32_t br = (bg >> 16) & 0xFF, bg_g = (bg >> 8) & 0xFF, bb = bg & 0xFF;
    uint32_t r = (fr * coverage + br * (4 - coverage)) >> 2;
    uint32_t g = (fg_g * coverage + bg_g * (4 - coverage)) >> 2;
    uint32_t b = (fb * coverage + bb * (4 - coverage)) >> 2;
    return (r << 16) | (g << 8) | b;
}

void font_draw_char_aa(int x, int y, uint8_t c, uint32_t fg, uint32_t bg) {
    if (!fb_is_vesa()) return;
    uint32_t fb_w = fb_get_width();
    uint32_t fb_h = fb_get_height();
    int h = font_get_active_height();
    /* AA output is same size as base glyph — we supersample at 2x then downsample */
    if (x < 0 || x + FONT_WIDTH > (int)fb_w || y < 0 || y + h > (int)fb_h) return;

    uint32_t* backbuf = fb_get_backbuffer();
    if (!backbuf) return;

    const uint8_t* glyph = font_get_glyph(c);

    /* For each output pixel, sample 2x2 from the "virtual 2x glyph" */
    for (int row = 0; row < h; row++) {
        /* Two source rows at 2x: row*2 and row*2+1, clamped */
        int r0 = row, r1 = (row + 1 < h) ? row + 1 : row;
        uint8_t bits0 = glyph[r0];
        uint8_t bits1 = glyph[r1];

        uint32_t* dst = &backbuf[(y + row) * fb_w + x];

        for (int col = 0; col < 8; col++) {
            /* Sample 2x2 block: current col and col+1, current row and row+1 */
            int c0 = col, c1 = (col + 1 < 8) ? col + 1 : col;
            int coverage = 0;
            if (bits0 & (0x80 >> c0)) coverage++;
            if (bits0 & (0x80 >> c1)) coverage++;
            if (bits1 & (0x80 >> c0)) coverage++;
            if (bits1 & (0x80 >> c1)) coverage++;
            dst[col] = blend_colors(fg, bg, coverage);
        }
    }
}

void font_draw_string_aa(int x, int y, const char* str, uint32_t fg, uint32_t bg) {
    int px = x;
    while (*str) {
        font_draw_char_aa(px, y, (uint8_t)*str, fg, bg);
        px += FONT_WIDTH;
        str++;
    }
}

/* ==========================================================================
 * Phase 15: Text Measurement
 * ========================================================================== */

int font_measure_string(const char* str) {
    int len = 0;
    while (*str) { len++; str++; }
    return len * FONT_WIDTH;
}

int font_measure_string_scaled(const char* str, int scale) {
    if (scale < 1) scale = 1;
    return font_measure_string(str) * scale;
}

